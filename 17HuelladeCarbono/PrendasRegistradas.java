    public void Playera(){

        double peso = 0.150 ;
        double mprima = 13.5;
        double maquina = 5.19; 
        double gasolina = 2.32;
        double trayecto = 5 ; 
        double lavadora = 0.69;
        double lavadas = 50; 
        double residuos = 3.00;
        double resultado1; 
        double resultado2; 
        double resultado3;
        double resultado4;
        double huella_de_carbono ; 
        double a,b,c,d;

        double energiaelectrica = 0.423;

        resultado1 = (peso*mprima);
        resultado2 = (peso*maquina);
        resultado3 = (gasolina*trayecto); 
        resultado4 = ((lavadora*lavadas)/5);
        a = (resultado1 * energiaelectrica);
        b = (resultado2 * energiaelectrica);
        c = (resultado3 * energiaelectrica);
        d = (resultado4 * energiaelectrica);

        huella_de_carbono = (a+b+c+d);

        JOptionPane.showMessageDialog(null, "Se estima que la huella de carbono de una playera  es :\n Talla : Mediana  \n Peso : " + peso + "\n Material : Algodon \n Huella de carbono : " + huella_de_carbono );


    }

